# NumPy is a Python library used for numerical computations, especially with arrays and matrices.

# Example of using NumPy in Python:
import numpy as np  # Importing the NumPy library

# Creating a NumPy array
arr = np.array([1, 2, 3, 4, 5])

# Accessing elements of the array
print(arr)  # Output: [1 2 3 4 5]

# Performing an operation on the array (e.g., multiplying by 2)
arr_multiplied = arr * 2
print(arr_multiplied)